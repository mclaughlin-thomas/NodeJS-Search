    function suggest(str, InputboxIdNum){
   
      var currentHint, temp, count;
      var domSuggestions = document.getElementById("suggestions");

      cleanup();

      if (str.length > 2)
         {
         count = 0;
         domSuggestions.innerHTML = domSuggestions.innerHTML + '<strong>Clickable Suggestions<strong><br /><br />';
         for (k = 0; k < hints.length; k++)
            {
            currentHint = hints[k];

            if (isPrefix(str, currentHint))
               {
               temp = '<a onclick="useSuggestion(' + k + ', ' + InputboxIdNum + ')" >' + currentHint + '</a><br />';
               domSuggestions.innerHTML = domSuggestions.innerHTML + temp;
               count++;
               }
            }
         if (count > 0)
            domSuggestions.style.visibility = "visible";
         }
   }
    
    
    function isPrefix(s, hint)
       {
       var k;
       var sLen = s.length;
       var hintLen = hint.length;
    
       if (sLen > hintLen)
          return false;
    
       s = s.toLowerCase();
    
       for (k = 0; k < sLen; k++)
          {
          if (s[k] != hint[k])
             {
             return false;
             }
          }
    
       return true;  // All of s matched the start of hint.
       }
    
    
    function cleanup()
       {
       domSuggestions = document.getElementById("suggestions");
       domSuggestions.innerHTML = "";
       domSuggestions.style.visibility = "hidden";
       }
    
    
    function useSuggestion(hint, InputboxIdNum)
       {
       var InputboxId = "Key" + InputboxIdNum;
       document.getElementById(InputboxId).value = hints[hint];
       }
    
    
